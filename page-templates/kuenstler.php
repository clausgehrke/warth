        <h2>Gedanken von Warth</h2>
        <p>Meine radikale Malerei beschränkt sich auf eine reduzierte und äusserst differenzierte Farberscheinung. Mir ist wichtig, die Reduktion alles Figürlichen und die puristische Konzentration auf die Klarheit der reinen Farbe und der dynamischen Lichtschwingungen im Raum. Parallelen findet man z.B. bei Yves Klein.</p>
        <p>Die spirituelle Auffassung der Farbe, die disziplinierte Beschränkung auf wenige Formen eröffnet eine große Freiheit für Emotionen. Meine Arbeiten haben keinen festen Bedeutungskern und keine eindeutige Botschaft. Sie sind offen für vielfältige Projektionen. Der Betrachter muß sich in die Bilder "hineinsehen".</p>
        <p>Radikale Monochromie entfaltet jeweils eigene Farbklänge, welche medidative Werte ausstrahlen und visuelle, transzendentale Reize schafft.</p>
        <p>So, wie diese Arbeiten still und konzentriert sind, so werden Sie für den Betrachter erst erfahrbar durch die Dauer und Konzentration des Sehens, dem sich die Prozesse, Unterschiede und Ereignisse der Farbe, Ihre Stofflichkeit, Nicht-Struktur, Bewegung, die ihr eigene Zeit und der ihr eigene Raum eröffnen.</p>
        <blockquote>Ich strebe die "letzten möglichen Bilder" an.</blockquote>
        <p>Die Ereignislosigkeit der Arbeiten ist nur scheinbar. Denn jede einzelne Arbeit zielt auf eine gefühlsmässige Wirkung, entwickelt in der Geschlossenheit und Reinheit der Oberfläche vom leisen Vibrieren bis hin zur fanatischen Leidenschaft and lässt jeweils den Rest einer anderen farbigen Aktion zu, einen Klang bewirkend, der das unwiederholbar Einmalige unterstreicht.</p>
        <p>Mondrian sagte: <blockquote>Die Domäne der Wahrheit ist reine Abstraktion</blockquote> </p>
        <p>WARTH sagt: <blockquote>Farbe und minimale Form sind der Schlüssel zur seelischen und geistigen Macht</blockquote>.</p>
        <p>Entwirklichte Kunst, ist reine Kunst, so wie z.B. bei Kandinsky. Den konkreten Inhalt eines Gemäldes in eine geistige Dimension zu übertragen. Das ist die Notwendigkeit.</p>
        <p>Malewitsch's Suprematie ist "das Übergeordnetsein über eine andere Macht".</p>
        <p>Es ist immer das Reduzierte was mich aufregt, anregt und erregt. Was mich berührt, ist die Spannung unter der scheinbar glatten Oberfläche.
        </p>
        <p>Nur ein Künstler der das Leben liebt, kann andere inspirieren. Nur, wer nicht aufhört nach Perfektion zu streben, bleibt lebendig.
        </p>
        <p>Mein Glaube an die Fähigkeit des Bildes, gültige Erfahrungen zu vermitteln, ungebrochen, gepaart mit einer überzeugten Neigung zu rationalen Ordnungsprinzipien als Quelle einer verborgenen, ästhetischen Schönheit ist ungebrochen. Erst im Raum sind die Bilder vollständig.
        </p>
        <p>In der Farbfeldmalerei taucht jenes Moment reflektierenden Innehaltens auf, das den allgemeinen "Überfluss" beliebig manipulierbarer Motive verhindert.
        </p>
        <p>Meine Arbeiten sehe ich in der Monochromie als quasi-religiösen, zweidimensionalen Andachtsraum, in dem sich der Betrachter - frei von jeder Ablenkung - verliert und geistig geläutert wiederfindet..
        </p>
        <p>Rot ist die Farbe der Liebe, der Leidenschaft, der aufbauenden Lebenskraft. Rot, das ist sofortige Energie. Sie wirkt direkt auf den Ätherkörper ein, der die körperliche Vitalität bestimmt. Rot belebt, verleiht Kraft und steht für Feuer, Hitze, Wut und die Materialisation auf der physischen Ebene.
        </p>
        <p>Weiss symbolisiert Reinheit und Jungfräulichkeit. Mit dem Licht, aus dem alle Farben hervorgehen, ist nicht das weisse Licht gemeint. Der Urstrahl, der das ganze Lichtspektrum enthält, ist klar und durchscheinend. Durch Weiss kann man jedoch nicht hindurchsehen, es ist nämlich opak, also lichtundurchlässig. Dadurch ist es für mich der überschwengliche Frieden.
        </p>
        <p>Schwarz, das schwarze Licht oder das, was manche Schamanen als spirituelles Licht bezeichnen, ist eine andere Form von Licht. Es führt an die Quelle des noch unsichtbaren Potentials. Die "Unfarbe" ist Tiefe, Unendlichkeit und sensible Weite. Schwarz ist für mich ästhetisch bis ins Unendliche.
        </p>
